    Qu'est ce que le levain ? 

    Le levain est maintenant répertorié comme d'origine animale. Les boulangeries au levain ne sont donc plus végannes. 
    L'origine animale se justifie par la présence de bacilles très variés et nombreux ( de l'orde du centaine de milliers de cellules ), bacilles présents sur la céréale sèche et dans l'air ( l'atmosphère ). 
    On comprend là l'importance de l'origine biologique des céréales, les pesticides altérant ces bacilles naturellement présents. 
    Le principe de développement et de fixation et la LACTOFERMATION, c'est à dire que la fermentation est lactique et non pas alcoolique. Les fermentations alcooliques se font sur le principe de dégagment de CO2 uniquement avec un effet de gonflement ( levure de boulanger ). Il n'y a aucune digestion autre que celle qui produit du gaz qui fait gonfler le pain. Sur une farine complète, cette fermentation lactique dégage l'acide phytique qui est très acidifiant donc toxique. Il faut exiger le levain sur les céréales complètes, sinon le pain est acidifiant et donc toxique ( le peu que le sel soit du sel blanc, là on atteint des sommets de toxicité terribles) 

    La levure de boulanger est très volatile est contaminante pour le levain. Travailler le levain et la levure de boulanger en même temps revient à travailler à la levure de boulanger. Le levain n'apportera qu'un peu de goût, mais ne digèrera rien car la plupart des bacilles auront été tués par ceux de la levure.


