        pain au levain, qui se caractérise par
        sa densité, sa mie irrégulière et son goût plus ou moins acidulé. 
        Il existe plusieurs façons de le préparer en
        fonction des régions du monde, des céréales utilisées, des méthodes de culture du levain, etc.
        Le levain est une matière vivante : il s'agit d'un élevage de (bonnes) bactéries à qui il faut apporter
        de la nourriture et des soins sous peine de le voir mourir...
