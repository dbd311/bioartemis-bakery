        D'une manière générale, un levain est une substance pouvant provoquer une fermentation. Mais le
        terme est surtout employé en boulangerie, où il désigne un mélange de farine et d'eau ayant fermenté
        grâce aux ferments naturels présents dans la farine et aux bactéries présentes dans l'air  
        (et non par l'apport de levure du boulanger industrielle).        
